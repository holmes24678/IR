def pulseIR(microsecs):
    while (microsecs>0):
        GPIO.output(IR_LED_PIN, GPIO.HIGH)
        time.sleep(0.001)
        GPIO.output(IR_LED_PIN, GPIO.LOW)
        time.sleep(0.001)
        microsecs -= 26

